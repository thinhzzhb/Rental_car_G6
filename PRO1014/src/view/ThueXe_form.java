    private void loadtb(List<ChiTietXe> _Lst) {
        _dtmX.setRowCount(0);
        for (ChiTietXe x : _Lst) {
            _dtmX.addRow(new Object[]{
                x.getIdXe(),
                x.getTenXe(),
                x.getTinhTrang(),
                x.getIdMS(),
                x.getGiaThue(),
                x.getBienSo(),
                x.getTrangThai()== 0 ? "Đang cho thuê":"Sẵn sàng"
            }
            );
        }
    }
    private void loadtbGH(List<CTHDViewModel> _Lst) {
        _dtmG =(DefaultTableModel) tblXeChon.getModel();
        _dtmG.setRowCount(0);
        for (CTHDViewModel CT : _Lst) {
            _dtmG.addRow(new Object[]{
                CT.getTenSp(),
                CT.getSoLuong(),
                CT.getDonGia(),
                CT.getBienSo()
            }
            );
        }
    }
    private void loadtbPg(List<PhieuGiao> _Lst) {
        _dtmP.setRowCount(0);
        for (PhieuGiao p : _Lst) {
            _dtmP.addRow(new Object[]{
                p.getIdPG(),
                p.getMaPG(),
                p.getNgayTao()
            }
            );
        }
    }
    private void showPGCT(List<PhieuGiaoCTViewModel> _Lst) {
        _dtmPCT.setRowCount(0);
        for (PhieuGiaoCTViewModel p : _Lst) {
            _dtmPCT.addRow(new Object[]{
                p.getTenKH(),
                p.getCCCD(),
                p.getSdt(),
                p.getDiaChi(),
                p.getSoGhe(),
                p.getSoKm(),
                p.getTenXe(),
                p.getTenMS(),
                p.getTinhTrang(),
                p.getBienSo(),
                p.getGiaThue(),
                p.getNgayTao()
            }
            );
        }
    }
    
